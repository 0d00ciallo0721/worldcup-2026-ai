# ── 查询接口 ──

def get_all_teams():
    conn = sqlite3.connect(DB_PATH); conn.row_factory = sqlite3.Row
    teams = conn.execute("SELECT * FROM teams ORDER BY group_name, name").fetchall()
    conn.close(); return teams

def get_team(team_id):
    conn = sqlite3.connect(DB_PATH); conn.row_factory = sqlite3.Row
    team = conn.execute("SELECT * FROM teams WHERE id=?", (team_id,)).fetchone()
    players = conn.execute("SELECT * FROM players WHERE team_id=? ORDER BY category, name", (team_id,)).fetchall()
    conn.close(); return team, players

def get_match(match_id):
    conn = sqlite3.connect(DB_PATH); conn.row_factory = sqlite3.Row
    m = conn.execute("SELECT * FROM matches WHERE id=?", (match_id,)).fetchone()
    if not m: conn.close(); return None,None,None,None,[],[]
    a = conn.execute("SELECT * FROM teams WHERE id=?",(m["team_a_id"],)).fetchone()
    b = conn.execute("SELECT * FROM teams WHERE id=?",(m["team_b_id"],)).fetchone()
    h2h = conn.execute("SELECT * FROM head_to_head WHERE (team_a_id=? AND team_b_id=?) OR (team_a_id=? AND team_b_id=?)",
        (m["team_a_id"],m["team_b_id"],m["team_b_id"],m["team_a_id"])).fetchone()
    ap = conn.execute("SELECT * FROM players WHERE team_id=?",(m["team_a_id"],)).fetchall()
    bp = conn.execute("SELECT * FROM players WHERE team_id=?",(m["team_b_id"],)).fetchall()
    conn.close(); return m,a,b,h2h,ap,bp

def get_all_matches_sorted():
    conn = sqlite3.connect(DB_PATH); conn.row_factory = sqlite3.Row
    matches = conn.execute("SELECT * FROM matches ORDER BY match_date").fetchall()
    result = []
    for m in matches:
        a = conn.execute("SELECT id,name,fifa_code,fifa_rank,has_detail FROM teams WHERE id=?",(m["team_a_id"],)).fetchone()
        b = conn.execute("SELECT id,name,fifa_code,fifa_rank,has_detail FROM teams WHERE id=?",(m["team_b_id"],)).fetchone()
        raw = m["match_date"]
        match = re.match(r"(\d+)月(\d+)日\s*(\d+):(\d+)", raw)
        sk = int(match.group(2))*10000+int(match.group(3))*100+int(match.group(4))*1 if match else 999999
        result.append({**dict(m),"team_a":dict(a),"team_b":dict(b),"sort_key":sk})
    conn.close(); result.sort(key=lambda x:x["sort_key"]); return result


# ── 报告缓存 ──

def get_cached_report(match_id):
    """读取缓存的报告，无缓存返回 None"""
    conn = sqlite3.connect(DB_PATH)
    row = conn.execute("SELECT report_html FROM report_cache WHERE match_id=?", (match_id,)).fetchone()
    conn.close()
    return row[0] if row else None


def save_report_cache(match_id, report_html):
    """写入报告缓存"""
    conn = sqlite3.connect(DB_PATH)
    conn.execute("INSERT OR REPLACE INTO report_cache (match_id, report_html) VALUES (?,?)",
                 (match_id, report_html))
    conn.commit()
    conn.close()
