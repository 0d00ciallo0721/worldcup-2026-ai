def init_db():
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    c.execute("""CREATE TABLE IF NOT EXISTS teams (
        id INTEGER PRIMARY KEY, name TEXT, fifa_code TEXT, group_name TEXT,
        fifa_rank INTEGER DEFAULT NULL, coach TEXT DEFAULT NULL,
        style TEXT DEFAULT NULL, strengths TEXT DEFAULT NULL,
        weaknesses TEXT DEFAULT NULL, has_detail INTEGER DEFAULT 0)""")
    c.execute("""CREATE TABLE IF NOT EXISTS players (
        id INTEGER PRIMARY KEY, team_id INTEGER, name TEXT, category TEXT,
        age INTEGER, position TEXT, status TEXT, recent_form TEXT,
        FOREIGN KEY (team_id) REFERENCES teams(id))""")
    c.execute("""CREATE TABLE IF NOT EXISTS matches (
        id INTEGER PRIMARY KEY, team_a_id INTEGER, team_b_id INTEGER,
        match_date TEXT, venue TEXT, group_name TEXT)""")
    c.execute("""CREATE TABLE IF NOT EXISTS head_to_head (
        id INTEGER PRIMARY KEY, team_a_id INTEGER, team_b_id INTEGER,
        summary TEXT)""")
    c.execute("""CREATE TABLE IF NOT EXISTS report_cache (
        match_id INTEGER PRIMARY KEY, report_html TEXT,
        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP)""")
    conn.commit()
    c.execute("SELECT COUNT(*) FROM teams")
    if c.fetchone()[0] == 0:
        seed_data(conn)
    conn.close()


